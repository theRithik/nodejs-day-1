const calculate={
    add : function add(num1, num2){
      return num1+num2
    },

    sub: function sub(num1, num2){
        return num1-num2
    },
    mul : function mul(num1, num2){
        return num1*num2
    },
    div : function div(num1, num2){
        return num1/num2
    }
        
    
}
module.exports= calculate